<h1 style='float:right; margin: 0.5em 8em;'>And How Simple!</h1>

<p>
	The "And How Simple!" library provides open-source Java solutions for rapid development of applications utilizing networking, serialization/persistence, concurrency control, and thread scheduling.  The primary goals of AHSlib are remove common drudgery from coding in java wherever possible, make writing multithreaded applications easier, and codify common design patterns so that good code becomes more self-documenting.  AHSlib  is tested for both stability and performance and comes complete with an abundance of abstract interfaces that made it sufficient to provide services to a diverse range of projects.
</p>
