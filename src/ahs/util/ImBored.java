package ahs.util;

public class ImBored extends UnsupportedOperationException {
	public ImBored() {
	}
	
	public ImBored(String $message) {
		super($message);
	}
	
	public ImBored(Throwable $cause) {
		super($cause);
	}
	
	public ImBored(String $message, Throwable $cause) {
		super($message, $cause);
	}
}
