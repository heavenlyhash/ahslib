package us.exultant.ahs.codec.ebon;

import us.exultant.ahs.core.*;

public class EbonException extends TranslationException {
	public EbonException() {
		super();
	}

	public EbonException(String $message, Throwable $cause) {
		super($message, $cause);
	}

	public EbonException(String $message) {
		super($message);
	}

	public EbonException(Throwable $cause) {
		super($cause);
	}
}
